/***************************************************************************//**
 * Initialize Micrium Kernel.
 ******************************************************************************/
void sl_micrium_kernel_init(void)
{
  RTOS_ERR err;

  OSInit(&err);
  EFM_ASSERT(RTOS_ERR_CODE_GET(err) == RTOS_ERR_NONE);
}

/***************************************************************************//**
 * Start Micrium Kernel.
 ******************************************************************************/
void sl_micrium_kernel_start(void)
{
  RTOS_ERR err;

  OSStart(&err);
  EFM_ASSERT(RTOS_ERR_CODE_GET(err) == RTOS_ERR_NONE);
}
